# / — ჩვეულებრივი გაყოფა
# აბრუნებს შედეგს ათწილადით (float), თუნდაც რიცხვები მთლიანად იყოფოდეს

print(10 / 2)  
print(7 / 2)   
print(5 / 4)    

# // — მთლიანი გაყოფა (floor division)
# აბრუნებს მხოლოდ მთელ ნაწილს (ათწილადის გარეშე)
# ყოველთვის „ქვემოთ“ ამრგვალებს

print(10 // 2)  
print(7 // 2)   
print(5 // 4)   

# % — ნაშთიანი გაყოფა (modulo)
# აბრუნებს გაყოფის ნაშთს

print(10 % 2)   
print(7 % 2)   
print(5 % 4)    
